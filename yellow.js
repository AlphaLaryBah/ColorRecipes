// YELLOW and BLUE
function displayColoYWB() {
    let displYB = document.getElementById('displYB');
    displYB.value = 'Green';
    displYB.style.color = 'white'
    displYB.style.backgroundColor = displYB.value
}

let cleaYWB = document.getElementById('cleaYWB').addEventListener('click', function () {
    let displYB = document.getElementById('displYB');
    displYB.style.display = 'none';
})


